/**
  * @brief  Rx Transfer completed callbacks.
  * @param  huart  Pointer to a UART_HandleTypeDef structure that contains
  *                the configuration information for the specified UART module.
  * @retval None
  */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
  if(huart->Instance == USART2)
  {

        if(UART2_RX_FinishFlag == 0)
        {

          UART2_RX_BUF[UART2_RX_Cnt] = value;

          if(UART2_RX_Cnt == 0)
          {
            __HAL_TIM_CLEAR_FLAG(&htim2,TIM_FLAG_UPDATE);
            if (HAL_TIM_Base_Start_IT(&htim2) != HAL_OK)
            {
              /* Starting Error */
              Error_Handler();
            }
          } 
          __HAL_TIM_SET_COUNTER(&htim2,0);
        
          UART2_RX_Cnt++;  

          if (UART2_RX_Cnt == UART2_MAX_RECV_LEN)
          {
            // LED_Toggle(LED0); 
            UART2_RX_FinishFlag = 1;
            HAL_TIM_Base_Stop_IT(&htim2); 
            __HAL_TIM_SET_COUNTER(&htim2,0);         
          }

          if(HAL_UART_Receive_IT(&huart2, (uint8_t *)&value, 1) != HAL_OK)
          {
            Error_Handler();
          }  
       }         
  }      

}


UART_StatusTypeDef Read_Uart2Buf(uint8_t *pData, uint16_t Size)
{
  uint8_t* dst, *src;
  uint8_t num;
  dst = pData; 
  src = UART2_RX_BUF;
  num = UART2_RX_Cnt;

  /*没有接收到的情况*/
  if(UART2_RX_Cnt <= 0)
  {
    return UART_buf_no;
  }

  /*接收数组比uart数组还小*/
  if (Size < UART2_RX_Cnt)
  {
    return UART_Rxbuf_small;
  }
  
  /*清除数组*/
  while (Size--)
  {
    *dst++ = 0;
  }
  dst = pData;

  /*将uart接收到的数组赋给目的数组，且清零*/
  while (num--)
  {
    *dst = *src;
    *src = 0;
    dst++;
    src++;
  }

  UART2_RX_Cnt =0;
  UART2_RX_FinishFlag = 0;
  
  return UART_buf_OK;

}

